 * - Button: a button tag is rendered
 *
 * @author LANDWEHR Computer und Software GmbH <programmierung@landwehr-software.de>
 * @package System.Web.UI.WebControls
 */
class TButtonTag extends TEnumerable
{
	const Input='Input';
	const Button='Button';
}
